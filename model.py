def vgg_block(num_convs, in_channels, out_channels):
    layers = []
    for _ in range(num_convs):
        layers.append(
            nn.Conv2d(in_channels, out_channels, kernel_size=3, padding=1))
        layers.append(nn.ReLU())
        in_channels = out_channels
    layers.append(nn.MaxPool2d(kernel_size=2, stride=2))
    return nn.Sequential(*layers)


class VGG(nn.Module):
    def __init__(self, conv_arch, in_channels):
        super(VGG, self).__init__()
        layers = []
        for num_conv, out_channels in conv_arch:
            layers.append(vgg_block(num_conv, in_channels, out_channels))
            in_channels = out_channels
        self.model = nn.Sequential(*layers, nn.Flatten(),
                                   nn.Linear(out_channels * 16 * 16, 4096),
                                   nn.ReLU(),
                                   #    nn.Dropout(0.5),
                                   nn.Linear(4096, 4096),
                                   nn.ReLU(),
                                   #    nn.Dropout(0.5),
                                   nn.Linear(4096, 2)
                                   )

    def forward(self, X):
        for layer in self.model:
            X = layer(X)
        return X


